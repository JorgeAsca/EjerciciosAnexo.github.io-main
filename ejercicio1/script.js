console.debug("¿Es estudiante?:", esEstudiante, ":", typeof esEstudiante); // Muestra: boolean
console.error("Habilidades:", habilidades, ":", typeof habilidades);       // Muestra: object
console.log("Dirección:", direccion, ":", typeof direccion);   // Muestra: object
